package org.eclipse.photran.internal.core.parser;

import org.eclipse.photran.internal.core.lexer.Token;
import org.eclipse.photran.internal.core.parser.Parser.*;
import java.util.List;

public class ASTBlockDataBodyConstructNode extends InteriorNode
{
    ASTBlockDataBodyConstructNode(Production production, List<CSTNode> childNodes, List<CSTNode> discardedSymbols)
    {
         super(production);
         
         for (Object o : childNodes)
             addChild((CSTNode)o);
         constructionFinished();
    }
    
    @Override protected void visitThisNodeUsing(ASTVisitor visitor)
    {
        visitor.visitASTBlockDataBodyConstructNode(this);
    }

    public ASTSpecificationPartConstructNode getSpecificationPartConstruct()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        if (getProduction() == Production.BLOCK_DATA_BODY_CONSTRUCT_36)
            return (ASTSpecificationPartConstructNode)getChild(0);
        else
            return null;
    }
}

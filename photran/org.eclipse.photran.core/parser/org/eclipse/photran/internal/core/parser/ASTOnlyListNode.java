package org.eclipse.photran.internal.core.parser;

import org.eclipse.photran.internal.core.lexer.Token;
import org.eclipse.photran.internal.core.parser.Parser.*;
import java.util.List;

public class ASTOnlyListNode extends InteriorNode
{
    protected int count = -1;

    ASTOnlyListNode(Production production, List<CSTNode> childNodes, List<CSTNode> discardedSymbols)
    {
         super(production);
         
         for (Object o : childNodes)
             addChild((CSTNode)o);
         constructionFinished();
    }

    /**
     * @return the number of ASTOnlyListNode nodes in this list
     */
    public int size()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods, including size(), cannot be called on the nodes of a CST after it has been modified");
        
        if (count >= 0) return count;
        
        count = 0;
        ASTOnlyListNode node = this;
        do
        {
            count++;
            node = node.getRecursiveNode();
        }
        while (node != null);
        
        return count;
    }
    
    ASTOnlyListNode recurseToIndex(int listIndex)
    {
        ASTOnlyListNode node = this;
        for (int depth = size()-listIndex-1, i = 0; i < depth; i++)
        {
            if (node == null) throw new IllegalArgumentException("Index " + listIndex + " out of bounds (size: " + size() + ")");
            node = (ASTOnlyListNode)node.getRecursiveNode();
        }
        return node;
    }
    
    @Override protected void visitThisNodeUsing(ASTVisitor visitor)
    {
        visitor.visitASTOnlyListNode(this);
    }

    public ASTOnlyNode getOnly(int listIndex)
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        ASTOnlyListNode node = recurseToIndex(listIndex);
        if (node.getProduction() == Production.ONLY_LIST_906)
            return (ASTOnlyNode)node.getChild(0);
        else if (node.getProduction() == Production.ONLY_LIST_907)
            return (ASTOnlyNode)node.getChild(2);
        else
            return null;
    }

    private ASTOnlyListNode getRecursiveNode()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        if (getProduction() == Production.ONLY_LIST_907)
            return (ASTOnlyListNode)getChild(0);
        else
            return null;
    }

    public Token getTComma(int listIndex)
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        ASTOnlyListNode node = recurseToIndex(listIndex);
        if (node.getProduction() == Production.ONLY_LIST_907)
            return (Token)node.getChild(1);
        else
            return null;
    }
}

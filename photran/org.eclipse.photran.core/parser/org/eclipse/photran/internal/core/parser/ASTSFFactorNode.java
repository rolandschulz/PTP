package org.eclipse.photran.internal.core.parser;

import org.eclipse.photran.internal.core.lexer.Token;
import org.eclipse.photran.internal.core.parser.Parser.*;
import java.util.List;

public class ASTSFFactorNode extends InteriorNode
{
    ASTSFFactorNode(Production production, List<CSTNode> childNodes, List<CSTNode> discardedSymbols)
    {
         super(production);
         
         for (Object o : childNodes)
             addChild((CSTNode)o);
         constructionFinished();
    }
    
    @Override protected void visitThisNodeUsing(ASTVisitor visitor)
    {
        visitor.visitASTSFFactorNode(this);
    }

    public ASTSFPrimaryNode getSFPrimary()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        if (getProduction() == Production.SFFACTOR_585)
            return (ASTSFPrimaryNode)getChild(0);
        else if (getProduction() == Production.SFFACTOR_586)
            return (ASTSFPrimaryNode)getChild(0);
        else
            return null;
    }

    public ASTPowerOpNode getPowerOp()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        if (getProduction() == Production.SFFACTOR_586)
            return (ASTPowerOpNode)getChild(1);
        else
            return null;
    }

    public ASTMultOperandNode getMultOperand()
    {
        if (treeHasBeenModified()) throw new IllegalStateException("Accessor methods cannot be called on the nodes of a CST after it has been modified");

        if (getProduction() == Production.SFFACTOR_586)
            return (ASTMultOperandNode)getChild(2);
        else
            return null;
    }
}
